public class Curso {
	private String titulo;
	private int qntAulas;
	
	public String getTitulo(){
		return this.titulo;
	}

	public void setTitulo( String titulo){
		this.titulo = titulo;
	}

	public int getQntAulas(){
		return this.qntAulas;
	}

	public void setQntAulas(int qntAulas){
		this.qntAulas = qntAulas;
	}
	
}
